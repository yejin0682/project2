package com.yg.dto;

public class BoardDTO {
	
	private int qbno;
	private String qtitle;
	private String qcontent;
	private String ID;
	private String qwritedate;
	private int qhitcount;
	private int ref;
	private int level;
	private int step;
	
	public int getQbno() {
		return qbno;
	}
	public void setQbno(int qbno) {
		this.qbno = qbno;
	}
	public String getQtitle() {
		return qtitle;
	}
	public void setQtitle(String qtitle) {
		this.qtitle = qtitle;
	}
	public String getQcontent() {
		return qcontent;
	}
	public void setQcontent(String qcontent) {
		this.qcontent = qcontent;
	}
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getQwritedate() {
		return qwritedate;
	}
	public void setQwritedate(String qwritedate) {
		this.qwritedate = qwritedate;
	}
	public int getQhitcount() {
		return qhitcount;
	}
	public void setQhitcount(int qhitcount) {
		this.qhitcount = qhitcount;
	}
	public int getRef() {
		return ref;
	}
	public void setRef(int ref) {
		this.ref = ref;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int getStep() {
		return step;
	}
	public void setStep(int step) {
		this.step = step;
	}
	
	
	

}
